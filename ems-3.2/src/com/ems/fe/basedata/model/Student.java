package com.ems.fe.basedata.model;
/**
 * 学生类
 * @author 陈云展
 *
 */
public class Student {
	private String sId;
	private String sName;
	private String sSex;
	private String sAge;
	private String sClasses;
	private String sDepartmentId;
	private String sDepartment;
	private String sGrade;
	private String sPhone;
	private String sEmail;
	private String sPic;
	private String sPassword;
	
	public String getSSex() {
		return sSex;
	}
	public void setSSex(String sex) {
		sSex = sex;
	}
	public String getSAge() {
		return sAge;
	}
	public void setSAge(String age) {
		sAge = age;
	}
	public String getSClasses() {
		return sClasses;
	}
	public void setSClasses(String classes) {
		sClasses = classes;
	}
	public String getSDepartmentId() {
		return sDepartmentId;
	}
	public void setSDepartmentId(String departmentId) {
		sDepartmentId = departmentId;
	}
	public String getSDepartment() {
		return sDepartment;
	}
	public void setSDepartment(String department) {
		sDepartment = department;
	}
	public String getSGrade() {
		return sGrade;
	}
	public void setSGrade(String grade) {
		sGrade = grade;
	}
	public String getSPhone() {
		return sPhone;
	}
	public void setSPhone(String phone) {
		sPhone = phone;
	}
	public String getSEmail() {
		return sEmail;
	}
	public void setSEmail(String email) {
		sEmail = email;
	}
	public String getSPic() {
		return sPic;
	}
	public void setSPic(String pic) {
		sPic = pic;
	}
	public String getSPassword() {
		return sPassword;
	}
	public void setSPassword(String password) {
		sPassword = password;
	}
	public String getSId() {
		return sId;
	}
	public void setSId(String id) {
		sId = id;
	}
	public String getSName() {
		return sName;
	}
	public void setSName(String name) {
		sName = name;
	}
	
}
